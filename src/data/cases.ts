/**
 * Кейсы Эксента: типы и данные для страницы списка и карточек.
 * Каждый кейс может иметь свою структуру блоков (task/solution/result или иное).
 */

export type CaseTag = {
  label: string;
  slug: string;
};

export type CaseRelated = {
  name: string;
  href: string;
};

export interface CaseBase {
  slug: string;
  title: string;
  /** Краткое описание для карточки и превью */
  excerpt: string;
  /** Теги для фильтра: направление услуг, отрасль */
  tags: CaseTag[];
  /** Клиент (можно обобщённо: «Девелоперская компания») */
  client: string;
  /** Год или период (опционально) */
  year?: string;
  /** Связанные услуги для блока внизу страницы кейса */
  relatedServices?: CaseRelated[];
}

/** Структура первого кейса: Задача → Решение (список) → Результат */
export interface CaseWithTaskSolutionResult extends CaseBase {
  structure: "task-solution-result";
  task: string;
  solution: string[];
  result: string;
}

/** Универсальная структура для кейсов с произвольными блоками */
export interface CaseCustom extends CaseBase {
  structure: "custom";
  sections: { title: string; content: string | string[] }[];
}

export type Case = CaseWithTaskSolutionResult | CaseCustom;

